#include <stdio.h>

int main (void){

	// declares the variables of i and j into an integer type
	int i, j ;
	// declares the variables of x and y into an float type
	float x, y;

	i = 10;
	j = 20;
	x = 43.2892f;
	y = 5527.0f;

	// %d displays the value in a decimal integer
	// %f displays the values in a floating point number with 6 decimal digits
	printf("i = %d,  j = %d,  x = %f, y = %f\n",  i,  j,  x,  y) ;

}
